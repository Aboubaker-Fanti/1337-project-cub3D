#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*t;

	t = lst;
	if (!lst | !f)
		return ;
	while (t)
	{
		f(t->content);
		t = t->next;
	}
}
