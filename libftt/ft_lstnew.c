#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*newlist;

	newlist = malloc(sizeof(t_list));
	if (!newlist)
		return (0);
	(*newlist).content = content;
	(*newlist).next = 0;
	return (newlist);
}
